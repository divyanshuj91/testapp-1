const STORAGE_KEY = 'civic_loop_language';
const DEFAULT_LANGUAGE = 'en';

/**
 * Get current language from localStorage
 * @returns {string} Language code (en, hi, etc.)
 */
export function getCurrentLanguage() {
    return localStorage.getItem(STORAGE_KEY) || DEFAULT_LANGUAGE;
}

/**
 * Set language and save to localStorage
 * @param {string} lang - Language code
 */
export function setLanguage(lang) {
    if (TRANSLATIONS[lang]) {
        localStorage.setItem(STORAGE_KEY, lang);
        return true;
    }
    return false;
}

/**
 * Get translated text for a key
 * @param {string} key - Translation key
 * @param {string} lang - Optional language override
 * @returns {string} Translated text or key if not found
 */
export function translate(key, lang = null) {
    const currentLang = lang || getCurrentLanguage();
    return TRANSLATIONS[currentLang]?.[key] || TRANSLATIONS[DEFAULT_LANGUAGE]?.[key] || key;
}

/**
 * Translate all elements with data-i18n attribute on the page
 */
export function translatePage() {
    const currentLang = getCurrentLanguage();
    const elements = document.querySelectorAll('[data-i18n]');

    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        const translation = translate(key, currentLang);

        // Update text content or placeholder
        if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
            if (element.placeholder) {
                element.placeholder = translation;
            }
        } else {
            element.textContent = translation;
        }
    });
}

